-- Staging activations model stub
-- Purpose: normalize raw envelopes into activation-family records while preserving provenance and confidence.

select
  cast(null as string) as record_id,
  cast(null as string) as destination_type,
  cast(null as string) as destination_id,
  cast(null as string) as action,
  cast(null as string) as canonical_payload_json,
  cast(null as string) as confidence_json,
  cast(null as string) as provenance_json
where 1 = 0;
